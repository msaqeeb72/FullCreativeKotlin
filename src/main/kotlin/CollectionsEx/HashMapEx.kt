package CollectionsEx

fun main() {
    //In this way we create an object of HashMap
    //Hash Map Contains Key Value pair
    //It has another constructor int size which defines initial size of Map
    var hMap = HashMap<String,Int>();

    //In this way we add elements in Hashmap
    hMap["one"] = 1
    hMap["two"] = 2
    hMap["three"] = 3
    //We also have put method
    hMap.put("four",4)
    println("after putting values $hMap")





}